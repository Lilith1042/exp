t.circle (10)
t.end_fill()
t.penup()



t.goto(0,110)
t.pendown()
t.color('black')
t.width(10)
t.goto(0,50)

t.penup()
t.goto(-40,55)
t.right(90)
t.pendown()
t.color('red')
t.circle(45,180)


